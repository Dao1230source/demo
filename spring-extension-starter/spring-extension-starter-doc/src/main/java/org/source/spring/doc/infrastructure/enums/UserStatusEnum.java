package org.source.spring.doc.infrastructure.enums;

/**
 * 用户状态枚举
 * 
 * <p>该枚举定义了用户账户的所有可能状态，用于控制用户的访问权限和账户生命周期管理。</p>
 * 
 * <p>使用场景：</p>
 * <ul>
 *   <li>用户注册后的初始状态设置为{@link #INACTIVE}</li>
 *   <li>用户完成激活后状态变更为{@link #ACTIVE}</li>
 *   <li>用户违反安全策略后状态变更为{@link #LOCKED}</li>
 *   <li>用户登录时检查账户状态以确定是否允许访问</li>
 * </ul>
 *
 * @author dao1230source
 * @since 1.0.0
 */
public enum UserStatusEnum {
    /**
     * 激活状态
     * 
     * <p>用户账户已激活，可以正常登录和使用系统功能。</p>
     * 
     * <p>处于此状态的用户拥有完整的系统访问权限。</p>
     */
    ACTIVE,
    
    /**
     * 未激活状态
     * 
     * <p>用户已注册但尚未激活账户，需要完成激活流程才能使用系统。</p>
     * 
     * <p>通常用于新用户注册后的初始状态，需要通过邮箱验证或手机验证完成激活。</p>
     */
    INACTIVE,
    
    /**
     * 锁定状态
     * 
     * <p>用户因安全原因被锁定，无法登录系统。</p>
     * 
     * <p>常见锁定原因包括：</p>
     * <ul>
     *   <li>连续多次密码输入错误</li>
     *   <li>检测到异常登录行为</li>
     *   <li>管理员手动锁定</li>
     * </ul>
     */
    LOCKED
}